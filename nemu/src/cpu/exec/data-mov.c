make_EHelper(cltd) {
  if (decoding.is_operand_size_16) {
    if(cpu.gpr[0]._16&0x8000)
	{
	  cpu.gpr[2]._16 = 0xffff;
	}
	else cpu.gpr[2]._16 = 0;
  }
  else {
    if(cpu.eax&0x80000000)
	{
      cpu.edx = 0xffffffff;
	}
	else cpu.edx = 0;
  }

  print_asm(decoding.is_operand_size_16 ? "cwtl" : "cltd");
}

